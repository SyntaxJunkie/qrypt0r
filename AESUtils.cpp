#define GETU32(plaintext) (((uint32)(plaintext)[0] << 24) ^ \
						((uint32)(plaintext)[1] << 16) ^ \
						((uint32)(plaintext)[2] <<  8) ^ \
						((uint32)(plaintext)[3] <<  0))

#define PUTU32(ciphertext, st) { (ciphertext)[0] = (unsigned char)((st) >> 24); \
							  (ciphertext)[1] = (unsigned char)((st) >> 16); \
							  (ciphertext)[2] = (unsigned char)((st) >>  8); \
							  (ciphertext)[3] = (unsigned char)((st) >>  0); }

/**
* Expand the cipher key into the encryption key schedule.
*
* @return the number of rounds for the given cipher key size.
*/
static int rijndaelSetupEncrypt(uint32* rk, const unsigned char* key, int keybits)
{
	int i = 0;
	uint32 temp;

	rk[0] = GETU32(key + 0);
	rk[1] = GETU32(key + 4);
	rk[2] = GETU32(key + 8);
	rk[3] = GETU32(key + 12);
	if (keybits == 128)
	{
		for (; ; )
		{
			temp = rk[3];
			rk[4] = rk[0] ^
				(Te4[(temp >> 16) & 0xff] & 0xff000000) ^
				(Te4[(temp >> 8) & 0xff] & 0x00ff0000) ^
				(Te4[(temp >> 0) & 0xff] & 0x0000ff00) ^
				(Te4[(temp >> 24) & 0xff] & 0x000000ff) ^
				rcon[i];
			rk[5] = rk[1] ^ rk[4];
			rk[6] = rk[2] ^ rk[5];
			rk[7] = rk[3] ^ rk[6];
			if (++i == 10)
			{
				return 10;
			}

			rk += 4;
		}
	}
	rk[4] = GETU32(key + 16);
	rk[5] = GETU32(key + 20);
	if (keybits == 192)
	{
		for (; ; )
		{
			temp = rk[5];
			rk[6] = rk[0] ^
				(Te4[(temp >> 16) & 0xff] & 0xff000000) ^
				(Te4[(temp >> 8) & 0xff] & 0x00ff0000) ^
				(Te4[(temp >> 0) & 0xff] & 0x0000ff00) ^
				(Te4[(temp >> 24) & 0xff] & 0x000000ff) ^
				rcon[i];
			rk[7] = rk[1] ^ rk[6];
			rk[8] = rk[2] ^ rk[7];
			rk[9] = rk[3] ^ rk[8];
			if (++i == 8)
			{
				return 12;
			}

			rk[10] = rk[4] ^ rk[9];
			rk[11] = rk[5] ^ rk[10];
			rk += 6;
		}
	}
	rk[6] = GETU32(key + 24);
	rk[7] = GETU32(key + 28);
	if (keybits == 256)
	{
		for (; ; )
		{
			temp = rk[7];
			rk[8] = rk[0] ^
				(Te4[(temp >> 16) & 0xff] & 0xff000000) ^
				(Te4[(temp >> 8) & 0xff] & 0x00ff0000) ^
				(Te4[(temp >> 0) & 0xff] & 0x0000ff00) ^
				(Te4[(temp >> 24) & 0xff] & 0x000000ff) ^
				rcon[i];
			rk[9] = rk[1] ^ rk[8];
			rk[10] = rk[2] ^ rk[9];
			rk[11] = rk[3] ^ rk[10];
			if (++i == 7)
			{
				return 14;
			}

			temp = rk[11];
			rk[12] = rk[4] ^
				(Te4[(temp >> 24) & 0xff] & 0xff000000) ^
				(Te4[(temp >> 16) & 0xff] & 0x00ff0000) ^
				(Te4[(temp >> 8) & 0xff] & 0x0000ff00) ^
				(Te4[(temp >> 0) & 0xff] & 0x000000ff);
			rk[13] = rk[5] ^ rk[12];
			rk[14] = rk[6] ^ rk[13];
			rk[15] = rk[7] ^ rk[14];
			rk += 8;
		}
	}

	return 0;
}

/**
* Expand the cipher key into the decryption key schedule.
*
* @return the number of rounds for the given cipher key size.
*/
static int rijndaelSetupDecrypt(uint32* rk, const unsigned char* key, int keybits)
{
	int nrounds, i, j;
	uint32 temp;

	/* expand the cipher key: */
	nrounds = rijndaelSetupEncrypt(rk, key, keybits);

	/* invert the order of the round keys: */
	for (i = 0, j = 4 * nrounds; i < j; i += 4, j -= 4)
	{
		temp = rk[i + 0];
		rk[i + 0] = rk[j + 0];
		rk[j + 0] = temp;

		temp = rk[i + 1];
		rk[i + 1] = rk[j + 1];
		rk[j + 1] = temp;

		temp = rk[i + 2];
		rk[i + 2] = rk[j + 2];
		rk[j + 2] = temp;

		temp = rk[i + 3];
		rk[i + 3] = rk[j + 3];
		rk[j + 3] = temp;
	}

	/* apply the inverse MixColumn transform to all round keys but the first and the last: */
	for (i = 1; i < nrounds; i++)
	{
		rk += 4;
		rk[0] =
			Td0[Te4[(rk[0] >> 24) & 0xff] & 0xff] ^
			Td1[Te4[(rk[0] >> 16) & 0xff] & 0xff] ^
			Td2[Te4[(rk[0] >> 8) & 0xff] & 0xff] ^
			Td3[Te4[(rk[0] >> 0) & 0xff] & 0xff];
		rk[1] =
			Td0[Te4[(rk[1] >> 24) & 0xff] & 0xff] ^
			Td1[Te4[(rk[1] >> 16) & 0xff] & 0xff] ^
			Td2[Te4[(rk[1] >> 8) & 0xff] & 0xff] ^
			Td3[Te4[(rk[1] >> 0) & 0xff] & 0xff];
		rk[2] =
			Td0[Te4[(rk[2] >> 24) & 0xff] & 0xff] ^
			Td1[Te4[(rk[2] >> 16) & 0xff] & 0xff] ^
			Td2[Te4[(rk[2] >> 8) & 0xff] & 0xff] ^
			Td3[Te4[(rk[2] >> 0) & 0xff] & 0xff];
		rk[3] =
			Td0[Te4[(rk[3] >> 24) & 0xff] & 0xff] ^
			Td1[Te4[(rk[3] >> 16) & 0xff] & 0xff] ^
			Td2[Te4[(rk[3] >> 8) & 0xff] & 0xff] ^
			Td3[Te4[(rk[3] >> 0) & 0xff] & 0xff];
	}

	return nrounds;
}

